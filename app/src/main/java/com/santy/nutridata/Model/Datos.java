    public void setPrioridad(String prioridad) { this.prioridad = prioridad; }

    public String getMedicoResponsable() { return medicoResponsable; }
    public void setMedicoResponsable(String medicoResponsable) { this.medicoResponsable = medicoResponsable; }

    public String getRespNombre() { return respNombre; }
    public void setRespNombre(String respNombre) { this.respNombre = respNombre; }

    public String getRespCedula() { return respCedula; }
    public void setRespCedula(String respCedula) { this.respCedula = respCedula; }

    public int getRespEdad() { return respEdad; }
    public void setRespEdad(int respEdad) { this.respEdad = respEdad; }

    public String getRespTelefono() { return respTelefono; }
    public void setRespTelefono(String respTelefono) { this.respTelefono = respTelefono; }

